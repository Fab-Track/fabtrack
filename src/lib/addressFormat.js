export function formatAddress(street, city, state, zip) {
  const parts = [];
  if (street) parts.push(street);
  const lastPart = [city, state].filter(Boolean).join(", ");
  const fullLast = [lastPart, zip].filter(Boolean).join(" ");
  if (fullLast) parts.push(fullLast);
  return parts.join(", ");
}

export function formatAddressFromRecord(record, prefix = "") {
  if (!record) return "";
  return formatAddress(
    record[`${prefix}street`] || "",
    record[`${prefix}city`] || "",
    record[`${prefix}state`] || "",
    record[`${prefix}zip`] || ""
  );
}